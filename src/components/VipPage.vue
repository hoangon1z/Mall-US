    initSwiper() {
        new Swiper(this.$refs.vipSwiper, {
        direction: 'horizontal',
        loop: false,
        spaceBetween: 30, // Đặt khoảng cách giữa các slide là 30px
        on: {
          slideChange: function () {
            let swiperWrapper = document.querySelector('.swiper-wrapper');
            let translateValue = -1 * this.activeIndex * (swiperWrapper.offsetWidth / this.slides.length + this.params.spaceBetween); // Cập nhật để tính toán khoảng cách
